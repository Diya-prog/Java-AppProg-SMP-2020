    private void loginButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginButtonActionPerformed

        String uid=userIDTF2.getText();
        String pwd=new String(PF2.getPassword());
        try
        {
            con = DriverManager.getConnection("jdbc:mysql://localhost/tododb","root","qwerty");
            stmt = con.createStatement();
            String sql="Select password,UserID from User where UserID='"+uid+"';";
            rs = stmt.executeQuery(sql);
            rs.first();
            String pass = rs.getString("password");
            if(pwd.equals(pass))
            {
                currentUserID=rs.getString("UserID");
                loginFrame.dispose();

                dashboard.setVisible(true);
                addTask.requestFocus();
                JOptionPane.showMessageDialog(null,"Login Successfully.\nWelcome "+currentUserID+".");
            }
            else
            {
                JOptionPane.showMessageDialog(null,"See, your userID or password is wrong.\nTry again!");
            }
            rs.close();
            con.close();
            stmt.close();
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
        refreshTable();
    }//GEN-LAST:event_loginButtonActionPerformed

