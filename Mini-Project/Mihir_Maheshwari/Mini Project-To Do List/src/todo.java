    private void refreshTable() {
        tm.setRowCount(0);
        try{
            con = DriverManager.getConnection("jdbc:mysql://localhost/tododb", "root", "qwerty");
            stmt = con.createStatement();
            String sql = "Select Task,status,p.priority,DOC,taskid from tasks t inner join priority p on t.priority = p.priority where userid='"+currentUserID+"' order by p.priorityid;";

            rs = stmt.executeQuery(sql);
            if(rs!=null)
            {
                while(rs.next())
                {
                    tm.addRow(new Object [] {rs.getString("Task"), rs.getBoolean("status"), rs.getString("priority"),rs.getDate("DOC"),rs.getInt("TaskID")});
                }
            }
            rs.close();
            stmt.close();
            con.close();
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }

