    private void createAccActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createAccActionPerformed

        if(userNameTF1.getText().toString().equals(""))
        {
            JOptionPane.showMessageDialog(rootPane, "Please Enter the User Name");
            userNameTF1.requestFocus();
        }
        else if(userNameTF1.getText().toString().equals(""))
        {
            JOptionPane.showMessageDialog(rootPane, "Please Enter the User Name");
            userNameTF1.requestFocus();
        }
        else if(PF1.getPassword().toString().equals("")||PF1.getPassword().toString().equals(null))
        {
            JOptionPane.showMessageDialog(rootPane, "Please Enter Password");
            PF1.requestFocus();
            return;
        }

        String name=userNameTF1.getText();
        String uid=userIDTF1.getText();
        String pass = new String(PF1.getPassword());
        try
        {
            con = DriverManager.getConnection("jdbc:mysql://localhost/tododb","root","qwerty");
            stmt = con.createStatement();
            String sql="INSERT INTO User(UserID, Name, DOJ, Password) VALUES ('"+uid+"', '"+ name +"', curdate(), '"+pass+"');";
            stmt.executeUpdate(sql);
            JOptionPane.showMessageDialog(rootPane,"Congrats! Account created Successfully");

            signUpFrame.dispose();
            this.setVisible(true);

            stmt.close();
            con.close();
        }
        catch(SQLIntegrityConstraintViolationException e)
        {
            JOptionPane.showMessageDialog(rootPane,e.getMessage());
            userIDTF1.setText(null);
            userIDTF1.requestFocus();
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null,e.getMessage());
            return;
        }
}//GEN-LAST:event_createAccActionPerformed

