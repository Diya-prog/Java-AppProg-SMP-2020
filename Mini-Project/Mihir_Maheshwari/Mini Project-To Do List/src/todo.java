    private void fillShowtask() {

        sTaskTF.setEditable(false);
        sDescTF.setEditable(false);
        sPriorityTF.setEditable(false);
        sStatusCheckbox.setEnabled(false);
        
        buttonGroup2.clearSelection();
        highRadioButton1.setSelected(false);
        mediumRadioButton1.setSelected(false);
        lowRadioButton1.setSelected(false);
        
        int taskid = (Integer)jTable1.getValueAt(jTable1.getSelectedRow(), 4);
        selectedTaskID=taskid;
        try
        {
            con = DriverManager.getConnection("jdbc:mysql://localhost/tododb", "root", "qwerty");
            stmt = con.createStatement();
            String sql = "Select Task,Description,priority,status from tasks where taskid="+taskid+";";
            rs = stmt.executeQuery(sql);
            rs.first();
            sTaskTF.setText(rs.getString("Task"));
            sDescTF.setText(rs.getString("Description"));
            sPriorityTF.setText(rs.getString("priority"));
            sStatusCheckbox.setSelected(rs.getBoolean("Status"));

            editTaskTF.setText(rs.getString("Task"));
            editDescTF.setText(rs.getString("Description"));
            editStatusCheckbox.setSelected(rs.getBoolean("status"));
            String prior=rs.getString("priority");
            if(prior.equalsIgnoreCase("high"))
            {
                highRadioButton1.setSelected(true);
            }
            else if(prior.equalsIgnoreCase("medium"))
            {
                mediumRadioButton1.setSelected(true);
            }
            else if(prior.equalsIgnoreCase("low"))
            {
                lowRadioButton1.setSelected(true);
            }

            rs.close();
            stmt.close();
            con.close();
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }

